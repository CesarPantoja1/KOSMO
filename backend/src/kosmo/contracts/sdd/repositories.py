from typing import Protocol, runtime_checkable

from kosmo.contracts.sdd.constitution import Constitution, FrozenConstitution
from kosmo.contracts.sdd.ears import EARSRequirement
from kosmo.contracts.sdd.events import PipelineEvent
from kosmo.contracts.sdd.feature import Feature
from kosmo.contracts.sdd.ids import FeatureId, ProjectId, SpecId, TaskId
from kosmo.contracts.sdd.project import Project
from kosmo.contracts.sdd.requirements_document import RequirementsDocument
from kosmo.contracts.sdd.spec import SpecDocument
from kosmo.contracts.sdd.tasks import Task


@runtime_checkable
class SpecRepository(Protocol):
    async def add(self, spec: SpecDocument) -> None: ...
    async def get(self, spec_id: SpecId) -> SpecDocument | None: ...
    async def update(self, spec: SpecDocument) -> None: ...
    async def list_by_project(self, project_id: ProjectId) -> list[SpecDocument]: ...


@runtime_checkable
class RequirementRepository(Protocol):
    async def add(self, spec_id: SpecId, requirements: list[EARSRequirement]) -> None: ...
    async def get_by_spec(self, spec_id: SpecId) -> list[EARSRequirement]: ...
    async def update(self, requirement: EARSRequirement) -> None: ...


@runtime_checkable
class TaskRepository(Protocol):
    async def add(self, spec_id: SpecId, tasks: list[Task]) -> None: ...
    async def get_by_spec(self, spec_id: SpecId) -> list[Task]: ...
    async def get_by_id(self, task_id: TaskId) -> Task | None: ...
    async def update(self, task: Task) -> None: ...


@runtime_checkable
class ConstitutionRepository(Protocol):
    async def upsert(
        self, project_id: ProjectId, constitution: Constitution
    ) -> FrozenConstitution: ...
    async def get(self, project_id: ProjectId) -> FrozenConstitution | None: ...


@runtime_checkable
class PipelineEventRepository(Protocol):
    async def append(self, event: PipelineEvent) -> None: ...
    async def get_for_spec(self, spec_id: SpecId) -> list[PipelineEvent]: ...
    async def get_since(self, spec_id: SpecId, cursor: int) -> list[PipelineEvent]: ...


@runtime_checkable
class ProjectRepository(Protocol):
    async def add(self, project: Project) -> None: ...
    async def get(self, project_id: ProjectId) -> Project | None: ...
    async def get_by_slug(self, slug: str) -> Project | None: ...
    async def list_all(self) -> list[Project]: ...
    async def update(self, project: Project) -> None: ...
    async def update_discovery_md(self, project_id: ProjectId, markdown: str) -> None: ...
    async def get_discovery_md(self, project_id: ProjectId) -> str | None: ...


@runtime_checkable
class FeatureRepository(Protocol):
    async def add(self, feature: Feature) -> None: ...
    async def get_by_project(self, project_id: ProjectId) -> list[Feature]: ...
    async def get(self, feature_id: FeatureId) -> Feature | None: ...
    async def get_by_slug(self, project_id: ProjectId, slug: str) -> Feature | None: ...
    async def delete(self, feature_id: FeatureId) -> None: ...
    async def update(self, feature: Feature) -> None: ...
    async def update_requirements(
        self, feature_id: FeatureId, requirements: RequirementsDocument
    ) -> None: ...
    async def update_requirements_document(self, feature_id: FeatureId, document: dict) -> None: ...
    async def get_requirements_document(self, feature_id: FeatureId) -> dict | None: ...
